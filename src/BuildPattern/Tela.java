package BuildPattern;

public class Tela {

	private int tamanho;
	private int bits;
	
	public int getTamanho() {
		return tamanho;
	}
	public void setTamanho(int tamanho) {
		this.tamanho = tamanho;
	}
	public int getBits() {
		return bits;
	}
	public void setBits(int bits) {
		this.bits = bits;
	}
	
	public Tela(int tamanho, int bits) {
		this.tamanho = tamanho;
		this.bits = bits;
	}
	
	
	
}
